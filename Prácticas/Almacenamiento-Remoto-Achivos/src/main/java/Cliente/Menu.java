    // Método para actualizar la etiqueta del directorio local
    private void actualizarDirectorioLocal(String ruta) {
        jLabel5.setText(ruta);
    }

    // Método para actualizar la etiqueta del directorio remoto
    private void actualizarDirectorioRemoto(String ruta) {
        jLabel4.setText(ruta);
    }

    // Método para listar el contenido de la carpeta local
    private void listarContenidoLocal() {
        String rutaCarpetaLocal = jLabel5.getText();
        File localFolder = new File(rutaCarpetaLocal);
        String[] localContents = localFolder.list();
        jList1.setListData(localContents);
    }

    // Método para listar el contenido de la carpeta remota
    private void listarContenidoRemoto() {
        // Obtener la ruta de la carpeta remota
        String rutaCarpetaRemota = jLabel4.getText();
        File localFolder = new File(rutaCarpetaRemota);
        String[] archivosRemotos = localFolder.list();
        jList2.setListData(archivosRemotos);
    }


    // Método para crear una carpeta local
    private void crearCarpetaLocal() {
        // Llamar al método correspondiente del cliente para crear una carpeta local
        cliente.crearCarpetaLocal();
        listarContenidoLocal(); // Actualizar la lista después de la creación
    }

    // Método para crear una carpeta remota
    private void crearCarpetaRemota() {
        // Llamar al método correspondiente del cliente para crear una carpeta remota
        cliente.crearCarpetaRemota();
        listarContenidoRemoto(); // Actualizar la lista después de la creación
    }

    // Método para eliminar una carpeta local
    private void eliminarCarpetaLocal() {
        // Llamar al método correspondiente del cliente para eliminar una carpeta local
        String carpetaEliminar = jList1.getSelectedValue();
        cliente.eliminarCarpetaLocal(carpetaEliminar);
        listarContenidoLocal(); // Actualizar la lista después de la eliminación
    }

    // Método para eliminar una carpeta remota
    private void eliminarCarpetaRemota() {
        // Llamar al método correspondiente del cliente para eliminar una carpeta remota
        String carpetaEliminarRemota = jList2.getSelectedValue();
        cliente.eliminarCarpetaRemota(carpetaEliminarRemota);
        listarContenidoRemoto(); // Actualizar la lista después de la eliminación
    }

    // Método para cambiar el directorio local
    private void cambiarDirectorioLocal() {
        // Abrir un selector de directorio y actualizar la etiqueta del directorio local
        JFileChooser chooser = new JFileChooser();
        chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int result = chooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = chooser.getSelectedFile();
            actualizarDirectorioLocal(selectedFile.getAbsolutePath());
            listarContenidoLocal(); // Actualizar la lista después del cambio
        }
    }

    // Método para cambiar el directorio remoto
    private void cambiarDirectorioRemoto() {
        cliente.cambiarDirectorioRemoto();
        String nuevaRuta = cliente.getRutaCarpetaRemota(); // Obtener la nueva ruta después de cambiar el directorio
        actualizarDirectorioRemoto(nuevaRuta); // Actualizar la etiqueta con la nueva ruta
        listarContenidoRemoto();
    }

    private void enviarArchivoLocalARemoto() {
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        int seleccion = fileChooser.showOpenDialog(null);
        if (seleccion == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            cliente.enviarArchivoRemoto(selectedFile.getAbsolutePath());
        }
        listarContenidoRemoto();
    }
    
    private void enviarCarpetaLocalARemoto() {
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int seleccion = fileChooser.showOpenDialog(null);
        if (seleccion == JFileChooser.APPROVE_OPTION) {
            File selectedFolder = fileChooser.getSelectedFile();
            cliente.enviarCarpetaRemota(selectedFolder.getAbsolutePath());
            listarContenidoRemoto(); // Actualizar la lista después de enviar la carpeta
        }
    }

